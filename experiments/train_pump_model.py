# =============================================================================
#  SmartPump — TinyML model trainer  (run this in Google Colab)
#  Trains a small neural network to predict "is the pump likely needed at this
#  hour-of-week?" and exports it as model.h for the ESP32 (ArduTFLite).
#
#  HOW TO USE:
#   1. Go to https://colab.research.google.com  -> New notebook
#   2. Paste this whole file into one cell
#   3. Runtime -> Run all   (takes ~1 minute)
#   4. It prints model.h AND downloads it. Send that file back.
#
#  This is a SEED model trained on a realistic synthetic usage pattern, so you
#  have a real neural net running on the device immediately. Later we retrain it
#  on the real data the ESP32 logs.
# =============================================================================

import numpy as np
import tensorflow as tf
import matplotlib.pyplot as plt

rng = np.random.default_rng(42)

# ---- 1. Feature encoding (MUST match the ESP32 side exactly) ----------------
# Time -> 4 cyclical features so "23:00" and "00:00" are close, "Sat" & "Sun"
# are close, etc. d = day of week, 0=Sunday..6=Saturday (matches ESP32 tm_wday).
def features(h, d):
    return [
        np.sin(2*np.pi*h/24), np.cos(2*np.pi*h/24),
        np.sin(2*np.pi*d/7),  np.cos(2*np.pi*d/7),
    ]

# ---- 2. Synthetic household water pattern -----------------------------------
# Probability the pump is needed at hour h on day d. Morning + evening peaks,
# quiet nights, weekends shifted later. This is the "ground truth" the NN learns.
def demand_prob(h, d):
    weekend = (d == 0 or d == 6)          # Sun / Sat
    p = 0.05                               # low night baseline
    if weekend:
        p = max(p, 0.70*np.exp(-((h-9)**2)/(2*2.0**2)))   # late, broad morning
    else:
        p = max(p, 0.80*np.exp(-((h-7)**2)/(2*1.2**2)))   # sharp 7am peak
    p = max(p, 0.85*np.exp(-((h-20)**2)/(2*1.8**2)))       # evening peak (all days)
    if 10 <= h <= 16:
        p = max(p, 0.20)                   # daytime trickle
    return min(p, 0.95)

# ---- 3. Generate training data (200 simulated weeks) ------------------------
X, Y = [], []
for _ in range(200):
    for d in range(7):
        for h in range(24):
            X.append(features(h, d))
            Y.append(1 if rng.random() < demand_prob(h, d) else 0)
X = np.array(X, dtype=np.float32)
Y = np.array(Y, dtype=np.float32)
print(f"training samples: {len(X)}")

# ---- 4. The neural network --------------------------------------------------
model = tf.keras.Sequential([
    tf.keras.layers.Input(shape=(4,)),
    tf.keras.layers.Dense(16, activation='relu'),
    tf.keras.layers.Dense(16, activation='relu'),
    tf.keras.layers.Dense(1,  activation='sigmoid'),   # outputs probability 0..1
])
model.compile(optimizer='adam', loss='binary_crossentropy', metrics=['accuracy'])
model.fit(X, Y, epochs=40, batch_size=64, validation_split=0.15, verbose=2)

# ---- 5. Sanity check: what did it learn? ------------------------------------
grid = np.array([[model.predict(np.array([features(h, d)]), verbose=0)[0,0]
                  for h in range(24)] for d in range(7)])
plt.figure(figsize=(10,3))
plt.imshow(grid, aspect='auto', cmap='Blues', origin='lower')
plt.yticks(range(7), ['Sun','Mon','Tue','Wed','Thu','Fri','Sat'])
plt.xlabel('hour'); plt.title('NN predicted P(pump needed)'); plt.colorbar()
plt.show()

# ---- 6. Convert to TensorFlow Lite (float32, tiny) --------------------------
converter = tf.lite.TFLiteConverter.from_keras_model(model)
converter.optimizations = [tf.lite.Optimize.DEFAULT]
tflite_model = converter.convert()
print(f"tflite size: {len(tflite_model)} bytes")

# ---- 7. Emit model.h (C array for the ESP32) --------------------------------
def to_c_header(data, var='pump_model'):
    lines = [f'// Auto-generated by train_pump_model.py — {len(data)} bytes',
             '#pragma once',
             f'alignas(8) const unsigned char {var}[] = {{']
    for i in range(0, len(data), 12):
        chunk = ', '.join(f'0x{b:02x}' for b in data[i:i+12])
        lines.append(f'  {chunk},')
    lines.append('};')
    lines.append(f'const unsigned int {var}_len = {len(data)};')
    return '\n'.join(lines)

header = to_c_header(tflite_model)
with open('model.h', 'w') as f:
    f.write(header)

print('\n================= model.h (copy everything below, or use the download) =================\n')
print(header)

# ---- 8. Download the file ---------------------------------------------------
try:
    from google.colab import files
    files.download('model.h')
except Exception:
    print('\n(not on Colab — model.h saved next to this script)')
